function cartDuplicates() {
  console.log("cartDuplicates");

  for( var i = 0; i < cart.length; i++ ) {
    for( var j = i+1; j < cart.length; j++ ) {
      if( cart[i].itemID == cart[j].itemID ) {
        cart[i].quantity++;
        cart.splice(j,1);
        j--;
      }
    }
  }
}

function cartSum() {
  console.log("cartSum");

  var cartSubTotal = 0;
  for (var i = 0; i < cart.length; i++) {
    cartSubTotal += (cart[i].price * cart[i].quantity);
  }
  return cartSubTotal;
}

